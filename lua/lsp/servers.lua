local function get_lsp_servers()
  local installed_packages =
    require("mason-registry").get_installed_package_names()
  local servers = {}
  local index = 1
  for _, value in ipairs(installed_packages) do
    servers[index] = server_mapping[value]
    index = index + 1
  end
  return servers
end

return get_lsp_servers
